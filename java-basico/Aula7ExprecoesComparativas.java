public class Aula7ExprecoesComparativas {
    public static void main(String[] args){
        // > maior
        // < menor
        // >= maior ou igual
        // <= menor ou igual
        // == igual
        // != diferente

        // exemplos

        int x;
        x = 5;
        System.out.println( x > 0 ); // true
        System.out.println( x == 3 ); // false
        System.out.println( 10 <= 30 ); // true
        System.out.println( x != 2 ); // true
    }
}
